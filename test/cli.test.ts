/**
 * Capture process.stderr.write for the duration of a callback; always
 * restores in finally so a failing assertion can never poison later tests.
 */
async function withCapturedStderr(fn: () => Promise<void>): Promise<string> {
  const original = process.stderr.write.bind(process.stderr);
  let captured = "";
  process.stderr.write = ((chunk: unknown): boolean => {
    captured += String(chunk);
    return true;
  }) as typeof process.stderr.write;
  try {
    await fn();
  } finally {
    process.stderr.write = original;
  }
  return captured;
}

/** Temp repo root holding one yarn project with a 0-byte yarn.lock. */
function makeEmptyLockfileTree(): { root: string; identity: string } {
  const root = mkdtempSync(join(tmpdir(), "licenses-cli-"));
  const projDir = join(root, "proj");
  mkdirSync(projDir);
  writeFileSync(join(projDir, "package.json"), "{}\n");
  writeFileSync(join(projDir, "yarn.lock"), "");
  return { root, identity: "proj" };
}

