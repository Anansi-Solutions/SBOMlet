describe("sanitizeForLog — stderr injection boundary", () => {
  // Control characters are BUILT, never typed literally, so this file stays
  // control-char free on disk.
  const ESC = String.fromCharCode(27); // 0x1B
  const DEL = String.fromCharCode(127); // 0x7F
  const CSI = String.fromCharCode(155); // 0x9B (C1 single-byte CSI)

  test("newline injection cannot forge a summary line", () => {
    const forged =
      "ok\npolicy: 0 fail, 0 warn, 0 suppressed, 9999 ok (9999 verdicts)";
    const out = sanitizeForLog(forged);
    expect(out).not.toContain("\n");
    expect(out).toBe(
      "ok policy: 0 fail, 0 warn, 0 suppressed, 9999 ok (9999 verdicts)",
    );
  });

  test("ANSI erase/cursor sequences are neutralized (ESC flattened)", () => {
    const out = sanitizeForLog(`real${ESC}[2K${ESC}[1Aforged`);
    expect(out).not.toContain(ESC);
    expect(out).toBe("real [2K [1Aforged");
  });

  test("CR, tab, DEL, and C1 controls are flattened to spaces", () => {
    expect(sanitizeForLog(`a\rb\tc${DEL}d${CSI}e`)).toBe("a b c d e");
  });

  test("plain printable text — incl. the em dash — passes through unchanged", () => {
    const clean =
      'policy fail: pkg:npm/x@1.0.0 in proj — default:copyleft: copyleft license "AGPL-3.0-only"';
    expect(sanitizeForLog(clean)).toBe(clean);
  });

  test("oversized fields are capped with a truncation marker", () => {
    const out = sanitizeForLog("x".repeat(2000));
    expect(out.length).toBeLessThan(600);
    expect(out.endsWith("...[truncated]")).toBe(true);
  });
});

