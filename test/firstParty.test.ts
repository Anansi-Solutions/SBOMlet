describe("yarnWorkspaceMembers — @workspace: resolution body lines", () => {
  test("direct entries: resolution body lines yield members with their literal relPaths", () => {
    const lockfile = [
      '"backend@workspace:backend":',
      "  version: 0.0.0-use.local",
      '  resolution: "backend@workspace:backend"',
      "  dependencies:",
      '    ms: "npm:2.1.3"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
      '"frontend@workspace:frontend":',
      "  version: 0.0.0-use.local",
      '  resolution: "frontend@workspace:frontend"',
      "  dependencies:",
      '    sax: "npm:1.4.1"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "backend", relPath: "backend", hasDependencies: true },
      { name: "frontend", relPath: "frontend", hasDependencies: true },
    ]);
  });

  test("glob-resolved literal: a scoped name's lazy match extends past the leading @", () => {
    // The shape a `libs/*` workspaces glob produces once yarn resolves it.
    const lockfile = [
      '"@scope/util@workspace:libs/util":',
      "  version: 0.0.0-use.local",
      '  resolution: "@scope/util@workspace:libs/util"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "@scope/util", relPath: "libs/util", hasDependencies: false },
    ]);
  });

  test("root entry: relPath '.' — a root-only lock yields exactly one member", () => {
    const lockfile = [
      '"demo-root@workspace:.":',
      "  version: 0.0.0-use.local",
      '  resolution: "demo-root@workspace:."',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "demo-root", relPath: ".", hasDependencies: false },
    ]);
  });

  test("header range descriptors (workspace:^) never contribute a member", () => {
    // Cross-workspace deps make the header carry a RANGE descriptor
    // (workspace:^), not a path — only the resolution body line is real.
    const lockfile = [
      '"backend@workspace:^, backend@workspace:backend":',
      "  version: 0.0.0-use.local",
      '  resolution: "backend@workspace:backend"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    const members = yarnWorkspaceMembers(lockfile);
    expect(members).toEqual([
      { name: "backend", relPath: "backend", hasDependencies: false },
    ]);
    expect(members.some((m) => m.relPath === "^")).toBe(false);
  });

  test("hasDependencies: true when the entry's own block has a dependencies: line, false otherwise", () => {
    const lockfile = [
      '"backend@workspace:backend":',
      "  version: 0.0.0-use.local",
      '  resolution: "backend@workspace:backend"',
      "  dependencies:",
      '    ms: "npm:2.1.3"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
      '"frontend@workspace:frontend":',
      "  version: 0.0.0-use.local",
      '  resolution: "frontend@workspace:frontend"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "backend", relPath: "backend", hasDependencies: true },
      { name: "frontend", relPath: "frontend", hasDependencies: false },
    ]);
  });

  test("a dependencies: block never bleeds across entries into a following member", () => {
    // backend's dependencies: block is followed by a NEW column-0 header
    // (frontend) before frontend's own (dependencies-less) body — frontend
    // must report false, not inherit backend's true.
    const lockfile = [
      '"backend@workspace:backend":',
      "  version: 0.0.0-use.local",
      '  resolution: "backend@workspace:backend"',
      "  dependencies:",
      '    ms: "npm:2.1.3"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
      '"frontend@workspace:frontend":',
      "  version: 0.0.0-use.local",
      '  resolution: "frontend@workspace:frontend"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    const members = yarnWorkspaceMembers(lockfile);
    expect(members.find((m) => m.name === "frontend")?.hasDependencies).toBe(
      false,
    );
  });

  test("dependenciesMeta: preceding dependencies: in the same entry does not confuse hasDependencies", () => {
    // dependenciesMeta always ACCOMPANIES a real dependencies: block in real
    // Yarn output, but the ORDER within the entry is not the parser's
    // assumption to break: dependenciesMeta appearing BEFORE dependencies:
    // must not suppress the true detection, and must not itself be mistaken
    // for a dependencies: line (DEPENDENCIES_BLOCK_RE is anchored to the
    // exact "dependencies:" line, never a prefix match).
    const lockfile = [
      '"backend@workspace:backend":',
      "  version: 0.0.0-use.local",
      '  resolution: "backend@workspace:backend"',
      "  dependenciesMeta:",
      "    ms:",
      "      optional: false",
      "  dependencies:",
      '    ms: "npm:2.1.3"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "backend", relPath: "backend", hasDependencies: true },
    ]);
  });

  test("a peer-dependencies-only entry (no dependencies: block) reports hasDependencies false, never a silent scan", () => {
    const lockfile = [
      '"peerlib@workspace:libs/peerlib":',
      "  version: 0.0.0-use.local",
      '  resolution: "peerlib@workspace:libs/peerlib"',
      "  peerDependencies:",
      '    react: "npm:^18.0.0"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "peerlib", relPath: "libs/peerlib", hasDependencies: false },
    ]);
  });

  test("unusual key order — languageName/linkType BEFORE resolution: — still yields the member (order within an entry is not assumed)", () => {
    const lockfile = [
      '"backend@workspace:backend":',
      "  languageName: unknown",
      "  linkType: soft",
      "  version: 0.0.0-use.local",
      '  resolution: "backend@workspace:backend"',
      "  dependencies:",
      '    ms: "npm:2.1.3"',
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "backend", relPath: "backend", hasDependencies: true },
    ]);
  });

  test("unusual key order — dependencies: BEFORE resolution: (alphabetically sorted keys) — still records hasDependencies (the flag belongs to the BLOCK, not to lines after the resolution)", () => {
    // Yarn itself emits resolution: before dependencies:, but the parser
    // claims order within an entry is not its assumption (the test above
    // locks the mirror case). A YAML normalizer sorting keys puts
    // dependencies before resolution — losing the flag there would warn
    // and skip a workspace that DOES declare dependencies, a false
    // "declares no dependencies" under-coverage.
    const lockfile = [
      '"backend@workspace:backend":',
      "  dependencies:",
      '    ms: "npm:2.1.3"',
      "  languageName: unknown",
      "  linkType: soft",
      '  resolution: "backend@workspace:backend"',
      "  version: 0.0.0-use.local",
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "backend", relPath: "backend", hasDependencies: true },
    ]);
  });

  test("CRLF lockfile text parses identically (trimEnd tolerance)", () => {
    const lockfile = [
      '"backend@workspace:backend":',
      "  version: 0.0.0-use.local",
      '  resolution: "backend@workspace:backend"',
      "  dependencies:",
      '    ms: "npm:2.1.3"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\r\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "backend", relPath: "backend", hasDependencies: true },
    ]);
  });

  test("garbage/binary-ish text returns [] and never throws", () => {
    expect(yarnWorkspaceMembers("")).toEqual([]);
    expect(yarnWorkspaceMembers("not a lockfile at all }{ \0\x01\x02")).toEqual(
      [],
    );
  });

  test("a non-workspace protocol resolution (npm:) contributes nothing", () => {
    const lockfile = [
      '"ms@npm:2.1.3":',
      "  version: 2.1.3",
      '  resolution: "ms@npm:2.1.3"',
      "  languageName: node",
      "  linkType: hard",
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([]);
  });

  test("hostile paths (traversal, absolute) are returned VERBATIM — containment is enforced at unit creation in the collect loop, not here", () => {
    const lockfile = [
      '"evil@workspace:../outside":',
      "  version: 0.0.0-use.local",
      '  resolution: "evil@workspace:../outside"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
      '"evil-abs@workspace:/etc/passwd":',
      "  version: 0.0.0-use.local",
      '  resolution: "evil-abs@workspace:/etc/passwd"',
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    expect(yarnWorkspaceMembers(lockfile)).toEqual([
      { name: "evil", relPath: "../outside", hasDependencies: false },
      { name: "evil-abs", relPath: "/etc/passwd", hasDependencies: false },
    ]);
  });

  test.each([
    ["scoped name", "@acme/web-app", "packages/web-app"],
    [
      "dots, dashes, underscores, digits",
      "my.pkg-name_2",
      "libs/my.pkg-name_2",
    ],
    ["name differs from its directory path", "api", "services/backend-api"],
    ["deep multi-segment path", "web", "packages/apps/web"],
    ["unicode name/path", "café-app", "packages/café-app"],
    [
      "node_modules-adjacent name",
      "node-modules-helper",
      "packages/node-modules-helper",
    ],
    ["prefix-colliding pair, shorter member", "app", "app"],
    ["prefix-colliding pair, longer member", "app-server", "app-server"],
    ["a directory literally named dot", "dot-named", "dot"],
    [
      "uppercase name differing in case from a lowercase path",
      "Frontend",
      "Frontend",
    ],
  ])(
    "%s: %s@workspace:%s parses identically to a plain canonical member",
    (_label, name, relPath) => {
      const lockfile = [
        `"${name}@workspace:${relPath}":`,
        "  version: 0.0.0-use.local",
        `  resolution: "${name}@workspace:${relPath}"`,
        "  dependencies:",
        '    ms: "npm:2.1.3"',
        "  languageName: unknown",
        "  linkType: soft",
        "",
      ].join("\n");

      expect(yarnWorkspaceMembers(lockfile)).toEqual([
        { name, relPath, hasDependencies: true },
      ]);
    },
  );
});

