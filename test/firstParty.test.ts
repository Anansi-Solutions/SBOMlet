describe("firstPartyNames — workspace/portal member set from lockfile text", () => {
  test("verified real-world portal and workspace entry headers yield their names", () => {
    // Both line shapes verified in frontend/yarn.lock (02-RESEARCH Pattern 3),
    // including the ::locator suffix on portal resolutions.
    const lockfile = [
      '"iframe-rpc-react@portal:../libraries/iframe-rpc-react::locator=myapp%40workspace%3A.":',
      "  version: 0.0.0-use.local",
      '"myapp@workspace:.":',
      "  version: 0.0.0-use.local",
    ].join("\n");

    expect(firstPartyNames(lockfile)).toEqual(
      new Set(["iframe-rpc-react", "myapp"]),
    );
  });

  test("scoped names capture the full @scope/name (lazy match does not stop at the leading @)", () => {
    const lockfile =
      '"@scratch/foo@workspace:apps/x":\n  version: 0.0.0-use.local\n';

    expect(firstPartyNames(lockfile)).toEqual(new Set(["@scratch/foo"]));
  });

  test("comma-separated descriptor headers test each descriptor, not just the line start", () => {
    const lockfile =
      '"iframe-rpc@npm:^1.0.0, iframe-rpc@portal:../libraries/iframe-rpc":\n' +
      "  version: 0.0.0-use.local\n";

    expect(firstPartyNames(lockfile)).toEqual(new Set(["iframe-rpc"]));
  });

  test("indented body lines and @npm:-only entries contribute nothing", () => {
    const lockfile = [
      '"lodash@npm:^4.17.21":',
      "  version: 4.17.21",
      '  resolution: "lodash@npm:4.17.21"',
      "  version: 0.0.0-use.local", // body line that LOOKS first-party-ish
      "semver@npm:^7.6.0:",
      "  version: 7.7.1",
      "",
    ].join("\n");

    expect(firstPartyNames(lockfile)).toEqual(new Set());
  });
});

describe("thirdPartyEntryCount — non-workspace/portal entry headers", () => {
  test("a zero-dependency Yarn-4 lockfile (__metadata + workspace self-entry) counts 0", () => {
    const lockfile = [
      '# This file is generated by running "yarn install" inside your project.',
      "# Manual changes might be lost - proceed with caution!",
      "",
      "__metadata:",
      "  version: 8",
      "  cacheKey: 10c0",
      "",
      '"proj@workspace:.":',
      "  version: 0.0.0-use.local",
      "  languageName: unknown",
      "  linkType: soft",
      "",
    ].join("\n");

    expect(thirdPartyEntryCount(lockfile)).toBe(0);
  });

  test("npm entries count; workspace/portal/__metadata headers and body lines do not", () => {
    const lockfile = [
      "__metadata:",
      "  version: 8",
      '"lodash@npm:^4.17.21":',
      "  version: 4.17.21",
      '"myapp@workspace:.":',
      "  version: 0.0.0-use.local",
      '"iframe-rpc-react@portal:../libraries/iframe-rpc-react::locator=myapp%40workspace%3A.":',
      "  version: 0.0.0-use.local",
      "semver@npm:^7.6.0:",
      "  version: 7.7.1",
    ].join("\n");

    expect(thirdPartyEntryCount(lockfile)).toBe(2);
  });

  test("a header with ANY workspace/portal descriptor resolves first-party and is not counted", () => {
    const lockfile =
      '"iframe-rpc@npm:^1.0.0, iframe-rpc@portal:../libraries/iframe-rpc":\n' +
      "  version: 0.0.0-use.local\n";

    expect(thirdPartyEntryCount(lockfile)).toBe(0);
  });

  test("a comment-only Yarn-1 placeholder lockfile counts 0 (genuinely zero entries)", () => {
    const lockfile =
      "# THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.\n" +
      "# yarn lockfile v1\n";

    expect(thirdPartyEntryCount(lockfile)).toBe(0);
  });

  test("Yarn-1 entry headers (no resolution protocol) count as third-party", () => {
    const lockfile = [
      "# yarn lockfile v1",
      "",
      "lodash@^4.17.21:",
      '  version "4.17.21"',
      '"@babel/code-frame@^7.0.0":',
      '  version "7.26.2"',
    ].join("\n");

    expect(thirdPartyEntryCount(lockfile)).toBe(2);
  });
});

